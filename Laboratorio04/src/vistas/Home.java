    private void btnAgregarDatosPruebaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDatosPruebaActionPerformed
        // TODO add your handling code here:
        String[] nombresPrueba = {"Ana", "Luis", "Maria", "Juan", "Sofia", "Carlos", "Laura", "Pedro", "Elena", "Diego", "Ana"}; 
        String[] apellidosPrueba = {"Garcia", "Martinez", "Lopez", "Perez", "Gomez", "Sanchez", "Diaz", "Torres", "Ruiz", "Flores", "Garcia"};

        int cantidad = 5; // O el nÃºmero que desees
        for (int i = 0; i < cantidad; i++) {
            String nombre = nombresPrueba[(int)(Math.random() * nombresPrueba.length)];
            String apellido = apellidosPrueba[(int)(Math.random() * apellidosPrueba.length)];

            Cliente c = new Cliente(
                UUID.randomUUID().toString().substring(0, 8),
                nombre,
                apellido,
                "555-" + (1000 + i),
                nombre.toLowerCase() + "." + apellido.toLowerCase() + "@example.com",
                "Av. Benavides " + (100 + i),
                "0000" + i
            );
            
            String resultadoLineal = tablaLineal.insertar(c);
            textALineal.append("Insertando (prueba) " + c.getNombres() + " " + c.getApellidos() + ":\n  " + resultadoLineal + "\n");

            String resultadoEncadenamiento = tablaEncadenamientoBST.insertar(c);
            textAEncadenamiento.append("Insertando (prueba) " + c.getNombres() + " " + c.getApellidos() + ":\n  " + resultadoEncadenamiento + "\n");
        }
        textALineal.append("------------------ FIN PRUEBAS -------------------\n");
        textAEncadenamiento.append("------------------ FIN PRUEBAS -------------------\n");
    }//GEN-LAST:event_btnAgregarDatosPruebaActionPerformed

    
    
